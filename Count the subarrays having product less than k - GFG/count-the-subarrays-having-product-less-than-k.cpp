class Solution{
  public:
    int countSubArrayProductLessThanK(const vector<int>& a, int n, long long k) {
        if(k<=1) return 0;
        long long mul=1; int count=0, l=0, r=0;
        while(r<n){
            mul*=a[r];
            while(mul >= k){
                mul/=a[l];
                l++;
            }
            count+=(r-l+1);
            r++;
        }
        return count;
    }
};

